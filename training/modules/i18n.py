def get_translation(language: str, key: str, **kwargs):
    """
    Get a translation for a given key and language
    """
    lang = language if language in TRANSLATIONS else "es"
    keys = key.split(".")
    value = TRANSLATIONS[lang]
    for k in keys:
        if isinstance(value, dict) and k in value:
            value = value[k]
        else:
            return key
    if kwargs:
        try:
            return value.format(**kwargs)
        except (KeyError, IndexError):
            return value
    return value

