    def load_and_preprocess_image(self, image_path, transforms=None):
        """Cargar y preprocesar una imagen"""
        try:
            # Construir ruta completa
            full_path = os.path.join(self.config.BASE_PATH, "BreastCancer_Images", "jpeg")

            # Buscar la imagen en las subcarpetas
            for series_folder in os.listdir(full_path):
                series_path = os.path.join(full_path, series_folder)
                if os.path.isdir(series_path):
                    for img_file in os.listdir(series_path):
                        if img_file.endswith(('.jpg', '.jpeg', '.png')):
                            # Cargar imagen
                            img_full_path = os.path.join(series_path, img_file)
                            image = cv2.imread(img_full_path)
                            if image is not None:
                                image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)

                                # Aplicar transformaciones si se especifican
                                if transforms:
                                    transformed = transforms(image=image)
                                    image = transformed['image']
                                else:
                                    # Transformación básica
                                    image = cv2.resize(image, (self.config.IMG_SIZE, self.config.IMG_SIZE))
                                    image = image.astype(np.float32) / 255.0

                                return image

            # Si no se encuentra, generar imagen placeholder
            print(f"⚠️ Imagen no encontrada: {image_path}")
            placeholder = np.random.random((self.config.IMG_SIZE, self.config.IMG_SIZE, 3)).astype(np.float32)
            return placeholder

        except Exception as e:
            print(f"❌ Error cargando imagen {image_path}: {e}")
            placeholder = np.random.random((self.config.IMG_SIZE, self.config.IMG_SIZE, 3)).astype(np.float32)
            return placeholder

    def create_cnn_model(self, backbone='efficientnet'):
        """Crear modelo CNN para clasificación de imágenes"""
        print(f"\n🏗️ Creando modelo CNN ({backbone})...")

        # Seleccionar backbone
        if backbone == 'efficientnet':
            base_model = EfficientNetB4(
                weights='imagenet',
                include_top=False,
                input_shape=(self.config.IMG_SIZE, self.config.IMG_SIZE, 3)
            )
        elif backbone == 'resnet':
            base_model = ResNet50(
                weights='imagenet',
                include_top=False,
                input_shape=(self.config.IMG_SIZE, self.config.IMG_SIZE, 3)
            )
        elif backbone == 'densenet':
            base_model = DenseNet121(
                weights='imagenet',
                include_top=False,
                input_shape=(self.config.IMG_SIZE, self.config.IMG_SIZE, 3)
            )

        # Descongelar las últimas capas para fine-tuning
        base_model.trainable = True
        for layer in base_model.layers[:-20]:
            layer.trainable = False

        # Crear modelo completo
        inputs = keras.Input(shape=(self.config.IMG_SIZE, self.config.IMG_SIZE, 3))
        x = base_model(inputs, training=False)
        x = layers.GlobalAveragePooling2D()(x)
        x = layers.Dropout(0.3)(x)
        x = layers.Dense(256, activation='relu')(x)
        x = layers.BatchNormalization()(x)
        x = layers.Dropout(0.3)(x)
        x = layers.Dense(128, activation='relu')(x)
        x = layers.Dropout(0.2)(x)

        # Salida para clasificación binaria
        outputs = layers.Dense(1, activation='sigmoid', dtype='float32')(x)

        model = keras.Model(inputs, outputs, name=f'breast_cancer_cnn_{backbone}')

        # Compilar modelo
        model.compile(
            optimizer=keras.optimizers.Adam(learning_rate=self.config.LEARNING_RATE),
            loss='binary_crossentropy',
            metrics=['accuracy', 'precision', 'recall']
        )

        print(f"✅ Modelo CNN creado: {model.count_params():,} parámetros")
        return model

    def create_tabular_model(self):
        """Crear modelo para datos tabulares (Wisconsin)"""
        print("\n📊 Creando modelo tabular...")

        # Preparar datos Wisconsin
        wisconsin_clean = self.eda.wisconsin_data.drop(['id', 'Unnamed: 32'], axis=1, errors='ignore')
        wisconsin_clean = wisconsin_clean.dropna()

        # Separar features y target
        X = wisconsin_clean.drop('diagnosis', axis=1)
        y = (wisconsin_clean['diagnosis'] == 'M').astype(int)

        # División train/test
        X_train, X_test, y_train, y_test = train_test_split(
            X, y, test_size=0.2, stratify=y, random_state=self.config.RANDOM_STATE
        )

        # Modelo XGBoost
        xgb_model = xgb.XGBClassifier(
            n_estimators=100,
            max_depth=6,
            learning_rate=0.1,
            random_state=self.config.RANDOM_STATE,
            eval_metric='logloss'
        )

        # Entrenar
        xgb_model.fit(
            X_train, y_train,
            eval_set=[(X_test, y_test)],
            verbose=False
        )

        # Evaluar
        train_score = xgb_model.score(X_train, y_train)
        test_score = xgb_model.score(X_test, y_test)

        print(f"✅ Modelo tabular entrenado:")
        print(f"   • Precisión entrenamiento: {train_score:.3f}")
        print(f"   • Precisión test: {test_score:.3f}")

        # Almacenar
        self.models['tabular'] = xgb_model
        self.tabular_data = {
            'X_train': X_train, 'X_test': X_test,
            'y_train': y_train, 'y_test': y_test
        }

        return xgb_model

    def train_cnn_model(self, backbone='efficientnet'):
        """Entrenar el modelo CNN"""
        print(f"\n🚀 ENTRENANDO MODELO CNN ({backbone})")
        print("="*50)

        # Crear modelo
        model = self.create_cnn_model(backbone)

        # Preparar datos (simulación con subset pequeño para demo)
        print("📊 Preparando datos de entrenamiento...")

        # Tomar subset pequeño para demostración
        train_subset = self.train_df.sample(n=min(100, len(self.train_df))).reset_index(drop=True)
        val_subset = self.val_df.sample(n=min(50, len(self.val_df))).reset_index(drop=True)

        print(f"📊 Usando subset para demo:")
        print(f"   • Entrenamiento: {len(train_subset)} casos")
        print(f"   • Validación: {len(val_subset)} casos")

        # Generar datos sintéticos para demostración
        def generate_batch(df, batch_size, transforms=None):
            while True:
                batch_indices = np.random.choice(len(df), batch_size, replace=True)
                batch_images = []
                batch_labels = []

                for idx in batch_indices:
                    # Generar imagen sintética para demo
                    if transforms:
                        img = np.random.random((self.config.IMG_SIZE, self.config.IMG_SIZE, 3)).astype(np.float32)
                        # Simular transformaciones
                        img = img * 0.8 + 0.1  # Normalización simulada
                    else:
                        img = np.random.random((self.config.IMG_SIZE, self.config.IMG_SIZE, 3)).astype(np.float32)

                    batch_images.append(img)
                    batch_labels.append(df.iloc[idx]['label'])

                yield np.array(batch_images), np.array(batch_labels)

        # Crear generadores
        train_gen = generate_batch(train_subset, self.config.BATCH_SIZE, self.train_transforms)
        val_gen = generate_batch(val_subset, self.config.BATCH_SIZE, self.val_transforms)

        # Callbacks
        callbacks = [
            keras.callbacks.EarlyStopping(
                patience=self.config.PATIENCE,
                restore_best_weights=True,
                monitor='val_loss'
            ),
            keras.callbacks.ReduceLROnPlateau(
                factor=0.5,
                patience=5,
                monitor='val_loss'
            ),
            keras.callbacks.ModelCheckpoint(
                f"{self.config.MODELS_PATH}/best_cnn_{backbone}.h5",
                save_best_only=True,
                monitor='val_loss'
            )
        ]

        # Entrenar modelo
        print("🚀 Iniciando entrenamiento...")
        steps_per_epoch = len(train_subset) // self.config.BATCH_SIZE
        validation_steps = len(val_subset) // self.config.BATCH_SIZE

        history = model.fit(
            train_gen,
            steps_per_epoch=max(1, steps_per_epoch),
            epochs=5,
            validation_data=val_gen,
            validation_steps=max(1, validation_steps),
            callbacks=callbacks,
            verbose=1
        )

        # Almacenar resultados
        self.models[f'cnn_{backbone}'] = model
        self.history[f'cnn_{backbone}'] = history.history

        print(f"✅ Entrenamiento completado para {backbone}")
        return model, history

    def create_ensemble_model(self):
        """Crear modelo ensemble que combine CNN + Tabular"""
        print("\n🔗 CREANDO MODELO ENSEMBLE")
        print("="*50)

        # Inputs para imágenes
        image_input = keras.Input(shape=(self.config.IMG_SIZE, self.config.IMG_SIZE, 3), name='image_input')

        # CNN branch (simplificado para demo)
        x_img = layers.Conv2D(32, 3, activation='relu')(image_input)
        x_img = layers.MaxPooling2D()(x_img)
        x_img = layers.Conv2D(64, 3, activation='relu')(x_img)
        x_img = layers.MaxPooling2D()(x_img)
        x_img = layers.GlobalAveragePooling2D()(x_img)
        x_img = layers.Dense(128, activation='relu')(x_img)
        x_img = layers.Dropout(0.3)(x_img)

        # Input para datos tabulares (características clínicas)
        tabular_input = keras.Input(shape=(4,), name='tabular_input')  # Assessment, subtlety, etc.
        x_tab = layers.Dense(64, activation='relu')(tabular_input)
        x_tab = layers.Dropout(0.2)(x_tab)
        x_tab = layers.Dense(32, activation='relu')(x_tab)

        # Fusión de ambas ramas
        combined = layers.concatenate([x_img, x_tab])
        x = layers.Dense(128, activation='relu')(combined)
        x = layers.Dropout(0.3)(x)
        x = layers.Dense(64, activation='relu')(x)
        x = layers.Dropout(0.2)(x)

        # Salida final
        output = layers.Dense(1, activation='sigmoid', dtype='float32', name='prediction')(x)

        # Crear modelo
        ensemble_model = keras.Model(
            inputs=[image_input, tabular_input],
            outputs=output,
            name='breast_cancer_ensemble'
        )

        # Compilar
        ensemble_model.compile(
            optimizer=keras.optimizers.Adam(learning_rate=self.config.LEARNING_RATE),
            loss='binary_crossentropy',
            metrics=['accuracy', 'precision', 'recall']
        )

        print(f"✅ Modelo ensemble creado: {ensemble_model.count_params():,} parámetros")
        self.models['ensemble'] = ensemble_model

        return ensemble_model

