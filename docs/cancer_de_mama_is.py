    def map_images_to_metadata(self):
        """Mapear imágenes encontradas con metadatos de CSV"""
        print(f"\n🔗 MAPEANDO IMÁGENES CON METADATOS")
        print("="*50)

        # Obtener SeriesInstanceUID de metadatos
        if 'SeriesInstanceUID' in self.meta.columns:
            series_in_meta = set(self.meta['SeriesInstanceUID'].unique())
            print(f"📋 Series en meta.csv: {len(series_in_meta)}")
        else:
            print("❌ No se encontró columna SeriesInstanceUID en metadatos")
            return 0

        # Buscar carpetas de imágenes
        if self.images_path.exists():
            image_folders = [d.name for d in self.images_path.iterdir() if d.is_dir()]
            series_with_images = set(image_folders)
            print(f"📁 Carpetas de imágenes: {len(series_with_images)}")

            # Encontrar correspondencias
            matching_series = series_in_meta.intersection(series_with_images)
            missing_images = series_in_meta - series_with_images
            extra_images = series_with_images - series_in_meta

            print(f"\n🔗 CORRESPONDENCIAS:")
            print(f"   ✅ Series con imágenes y metadatos: {len(matching_series)}")
            print(f"   ❌ Series en meta.csv sin imágenes: {len(missing_images)}")
            print(f"   ⚠️ Carpetas de imágenes sin metadatos: {len(extra_images)}")

            if len(matching_series) > 0:
                print(f"\n📊 Ejemplos de series con correspondencia:")
                for i, series_id in enumerate(list(matching_series)[:3]):
                    folder_path = self.images_path / series_id
                    if folder_path.exists():
                        images_in_folder = len(list(folder_path.glob("*.jpg"))) + \
                                         len(list(folder_path.glob("*.jpeg")))
                        print(f"   {i+1}. {series_id[:50]}...")
                        print(f"      📷 Imágenes: {images_in_folder}")

                        # Buscar en metadatos
                        meta_info = self.meta[self.meta['SeriesInstanceUID'] == series_id]
                        if not meta_info.empty:
                            series_desc = meta_info['SeriesDescription'].iloc[0]
                            image_count = meta_info['ImageCount'].iloc[0]
                            print(f"      📋 Descripción: {series_desc}")
                            print(f"      📊 ImageCount esperado: {image_count}")

            return len(matching_series)
        else:
            print("❌ Carpeta de imágenes no existe")
            return 0

    def check_image_files(self, sample_size=5):
        """Verificar archivos de imagen (búsqueda recursiva)"""
        print(f"\n🖼️ VERIFICANDO ARCHIVOS DE IMAGEN")
        print("="*50)

        if self.images_path.exists():
            print(f"📂 Explorando: {self.images_path}")

            # Buscar subcarpetas (SeriesInstanceUID folders)
            subdirs = [d for d in self.images_path.iterdir() if d.is_dir()]
            print(f"📁 Subcarpetas encontradas: {len(subdirs)}")

            if len(subdirs) > 0:
                print(f"📁 Ejemplos de subcarpetas:")
                for i, subdir in enumerate(subdirs[:3]):
                    print(f"   {i+1}. {subdir.name}")

            # Buscar archivos de imagen recursivamente
            image_extensions = ['*.jpg', '*.jpeg', '*.png', '*.dcm']
            all_images = []

            print(f"\n🔍 Buscando imágenes recursivamente...")
            for ext in image_extensions:
                # Búsqueda recursiva con **
                recursive_images = list(self.images_path.rglob(ext))
                all_images.extend(recursive_images)
                if len(recursive_images) > 0:
                    print(f"   {ext}: {len(recursive_images)} archivos")

            print(f"\n📷 Total archivos de imagen encontrados: {len(all_images)}")

            if len(all_images) > 0:
                print(f"\n🔍 Analizando muestra de {min(sample_size, len(all_images))} archivos:")

                for i, img_file in enumerate(all_images[:sample_size]):
                    try:
                        # Mostrar estructura de carpetas
                        relative_path = img_file.relative_to(self.images_path)
                        print(f"\n   📁 Archivo {i+1}: {relative_path}")

                        # Intentar cargar con CV2
                        img = cv2.imread(str(img_file), cv2.IMREAD_GRAYSCALE)
                        if img is not None:
                            height, width = img.shape
                            file_size = img_file.stat().st_size / (1024*1024)  # MB
                            print(f"      ✅ Dimensiones: {width}x{height}")
                            print(f"      📊 Tamaño: {file_size:.2f} MB")
                            print(f"      🔢 Rango píxeles: {img.min()}-{img.max()}")
                        else:
                            print(f"      ❌ No se pudo cargar con OpenCV")
                    except Exception as e:
                        print(f"      ❌ Error: {str(e)}")

                # Estadísticas por carpeta
                print(f"\n📊 ESTADÍSTICAS POR CARPETA:")
                folder_stats = {}
                for img_file in all_images:
                    folder_name = img_file.parent.name
                    if folder_name not in folder_stats:
                        folder_stats[folder_name] = 0
                    folder_stats[folder_name] += 1

                print(f"   Total carpetas con imágenes: {len(folder_stats)}")
                print(f"   Promedio imágenes por carpeta: {np.mean(list(folder_stats.values())):.1f}")
                print(f"   Rango: {min(folder_stats.values())}-{max(folder_stats.values())} imágenes")

                return len(all_images)
            else:
                print("❌ No se encontraron archivos de imagen")
                print("💡 Verificar estructura de carpetas y extensiones de archivo")
                return 0
        else:
            print("❌ Carpeta de imágenes no existe")
            print(f"💡 Ruta esperada: {self.images_path}")
            return 0

